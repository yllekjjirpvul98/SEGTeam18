package Model;

public class Bounce {

    int time;
    int numOfPageVisited;

    public void setNumOfPageVisited(int numOfPageVisited) {
        this.numOfPageVisited = numOfPageVisited;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getNumOfPageVisited() {
        return numOfPageVisited;
    }

    public int getTime() {
        return time;
    }

}
